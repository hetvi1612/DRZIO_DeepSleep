package drzio.insomnia.treatment.bedtime.yoga.sleep.models;

public class Weekmodel {
    String wname;
    String totaldays;
    String completedays;
    String isweekdata;

    public String getIsweekdata() {
        return isweekdata;
    }

    public void setIsweekdata(String isweekdata) {
        this.isweekdata = isweekdata;
    }

    public String getWname() {
        return wname;
    }

    public void setWname(String wname) {
        this.wname = wname;
    }

    public String getTotaldays() {
        return totaldays;
    }

    public void setTotaldays(String totaldays) {
        this.totaldays = totaldays;
    }

    public String getCompletedays() {
        return completedays;
    }

    public void setCompletedays(String completedays) {
        this.completedays = completedays;
    }
}
